package com.example.bookmanagment.Modal;

public class Room
{
    private int id;
    private int NumberOfShelf;

    public int getId()
    {
        return id;
    }

    public void setId(int id)
    {
        this.id = id;
    }

    public int getShelfNumber()
    {
        return NumberOfShelf;
    }

    public void setShelfNumber(int shelf)
    {
        NumberOfShelf = shelf;
    }
}
